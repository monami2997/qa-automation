//Greater between two numbers
let num1=20;
let num2=15;
let greaterValue= num2>num1 ? `${num2} is greater` : `${num1} is greater`;
console.log(`Greater num : ${greaterValue}`);


// greater between three numbers
let num3=10
let greaterValueinThree= (num2>num1 && num2 > num3) ? `num2 = ${num2} is greater` : 
(num3>num1 && num3>num2) ? `num3 = ${num3} is greater`: `num1 = ${num1} is greater`;
console.log(`Greater num : ${greaterValueinThree}`);



